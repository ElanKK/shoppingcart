package com.mindteck.spring.model;

/**
 * Model to hold the cart line item details
 * @author Geetha Subramanian
 *
 */
public class LineItem {
	public int intProdId;
	public String strImageName;
	public String strProdName;
	public int intQty;
	public String strBrand;
	public String strDesc;
	public double dblPrice;
	public int intInputQty;
	public double dblTotal;
	/**
	 * @return the intProdId
	 */
	public int getIntProdId() {
		return intProdId;
	}
	/**
	 * @param intProdId the intProdId to set
	 */
	public void setIntProdId(int intProdId) {
		this.intProdId = intProdId;
	}
	/**
	 * @return the strImageName
	 */
	public String getStrImageName() {
		return strImageName;
	}
	/**
	 * @param strImageName the strImageName to set
	 */
	public void setStrImageName(String strImageName) {
		this.strImageName = strImageName;
	}
	/**
	 * @return the strProdName
	 */
	public String getStrProdName() {
		return strProdName;
	}
	/**
	 * @param strProdName the strProdName to set
	 */
	public void setStrProdName(String strProdName) {
		this.strProdName = strProdName;
	}
	/**
	 * @return the intQty
	 */
	public int getIntQty() {
		return intQty;
	}
	/**
	 * @param intQty the intQty to set
	 */
	public void setIntQty(int intQty) {
		this.intQty = intQty;
	}
	/**
	 * @return the strBrand
	 */
	public String getStrBrand() {
		return strBrand;
	}
	/**
	 * @param strBrand the strBrand to set
	 */
	public void setStrBrand(String strBrand) {
		this.strBrand = strBrand;
	}
	/**
	 * @return the strDesc
	 */
	public String getStrDesc() {
		return strDesc;
	}
	/**
	 * @param strDesc the strDesc to set
	 */
	public void setStrDesc(String strDesc) {
		this.strDesc = strDesc;
	}
	/**
	 * @return the dblPrice
	 */
	public double getDblPrice() {
		return dblPrice;
	}
	/**
	 * @param dblPrice the dblPrice to set
	 */
	public void setDblPrice(double dblPrice) {
		this.dblPrice = dblPrice;
	}
	/**
	 * @return the intInputQty
	 */
	public int getIntInputQty() {
		return intInputQty;
	}
	/**
	 * @param intInputQty the intInputQty to set
	 */
	public void setIntInputQty(int intInputQty) {
		this.intInputQty = intInputQty;
	}
	/**
	 * @return the dblTotal
	 */
	public double getDblTotal() {
		return dblTotal;
	}
	/**
	 * @param dblTotal the dblTotal to set
	 */
	public void setDblTotal(double dblTotal) {
		this.dblTotal = dblTotal;
	}
	
	

}
